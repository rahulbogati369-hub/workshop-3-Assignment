public class MathOperations {
    public static void main(String[] args) {
        
        boolean p = true, q = false;
        System.out.println("\nLogical Operators:");
        System.out.println("p && q = " + (p && q));
        System.out.println("p || q = " + (p || q));
        System.out.println("!p = " + (!p));
        
    }
    
}

